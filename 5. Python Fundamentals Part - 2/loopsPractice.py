# Loops in Python: Problems

# Problem 1: Number from 1 to 5
i = 1
while i <= 5:
    print(f"Number: {i}")
    i = i + 1

# Problem 2: Reverse of am Number
j = 5
while j >= 1:
    print(f"Number: {j}")
    j = j - 1

# Problem 3: Multiplication of n table
print("\nMultiplication of n table entered by user:")
num = int(input("Enter a number: "))

i = 1
while i <= 10:
    print(f"{num} * {i} = {num * i}")
    i = i + 1
